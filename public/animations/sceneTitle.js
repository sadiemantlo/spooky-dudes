document.addEventListener('DOMContentLoaded', () => {
async function sceneTitle1() {
  
if (window.location.pathname === '/story/1'){
  anime.set(".sceneTitle1", { opacity: 0 });

  anime({
    targets: ".sceneTitle1",
    opacity: 1,
    duration: 20000,
    delay: 5000,
  });
}
}
sceneTitle1()
})

